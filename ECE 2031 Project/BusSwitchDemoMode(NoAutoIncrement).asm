ORG 0

;Load Rainbow onto BUS 2	
	LOADI 2
	OUT PXL_S
	
	LOADI 0
	OUT PXL_A
	STORE ADDR
	
LOOP: 
	LOADI 1
	OUT LEDs
	
	

	LOAD RED
	OUT PXL_D
		
	
	
	LOAD ORANGE
	OUT PXL_D
		
	
		
	
	
	LOAD YELLOW
	OUT PXL_D
		  
	
	
		
	LOAD GREEN
	OUT PXL_D
		  
	
		
		
		
	LOAD BLUE
	OUT PXL_D
		  
	
		
		
	LOAD PURPLE
	OUT PXL_D
		
	LOAD RAINBOwROAD
	ADDI -1
	STORE RAINBOwROAD
	JZERO NEXT
	JUMP LOOP
		  
NEXT:	
;Load Teal/Pink Alternating onto Bus 3	
	LOADI 3
	Out PXL_S
	  
	LOADI 0
	OUT PXL_A
	STORE ADDR
	 
LOOPTWO:
	LOADI 2
	OUT LEDs
	  
	
	

	LOAD PINK
	OUT PXL_D
	  
	
	
	LOAD TEAL
	OUT PXL_D
	
	LOAD TropicalBreeze
	ADDI -1
	STORE TropicalBreeze
	JZERO LoopThree
	Jump LOOpTWO
	  
	
 LoopThree:
 ;Switch Between the Two Busses
	LOADI 4
	OUT LEDs
	IN Switches
	AND SwitchZero
	JPOS TWO
	
	IN Switches
	AND SwitchOne
	JPOS THREE
	
	JUMP LoopTHree
	
TWO:
	LOADI 2
	OUT PXL_L
	OUT LEDs
	JUMP LoopThree
	
THREE:
	LOADI 3
	OUT PXL_L
	OUT LEDs
	JUMP LOOPTHREE
	
	

	
	  
